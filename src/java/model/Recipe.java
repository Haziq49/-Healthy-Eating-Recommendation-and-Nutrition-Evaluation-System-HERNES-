package model;

/**
 *
 * @author haziq
 */

import java.io.Serializable;

public class Recipe implements Serializable {

    private int recipeID;          // AUTO_INCREMENT → int
    private String recipeName;
    private String instruction;
    private double totalEnergy;
    private double totalProtein;
    private double totalFat;
    private double totalCarb;

    public Recipe() {
    }

    // Constructor for READ / UPDATE (ID already exists)
    public Recipe(int recipeID, String recipeName, String instruction) {
        this.recipeID = recipeID;
        this.recipeName = recipeName;
        this.instruction = instruction;
    }

    // Constructor for CREATE (ID generated by DB)
    public Recipe(String recipeName, String instruction) {
        this.recipeName = recipeName;
        this.instruction = instruction;
    }

    public int getRecipeID() {
        return recipeID;
    }

    public void setRecipeID(int recipeID) {
        this.recipeID = recipeID;
    }

    public String getRecipeName() {
        return recipeName;
    }

    public void setRecipeName(String recipeName) {
        this.recipeName = recipeName;
    }

    public String getInstruction() {
        return instruction;
    }

    public void setInstruction(String instruction) {
        this.instruction = instruction;
    }

    public double getTotalEnergy() {
        return totalEnergy;
    }

    public void setTotalEnergy(double totalEnergy) {
        this.totalEnergy = totalEnergy;
    }

    public double getTotalProtein() {
        return totalProtein;
    }

    public void setTotalProtein(double totalProtein) {
        this.totalProtein = totalProtein;
    }

    public double getTotalFat() {
        return totalFat;
    }

    public void setTotalFat(double totalFat) {
        this.totalFat = totalFat;
    }

    public double getTotalCarb() {
        return totalCarb;
    }

    public void setTotalCarb(double totalCarb) {
        this.totalCarb = totalCarb;
    }
}

